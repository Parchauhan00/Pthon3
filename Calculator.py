first = input("Enter first number")

operator = input("Enter operator (+,-,*,/,%):")

second = input("Enter second number: ")

if operator == "+":
    print(first + second)

elif operator == "-":
    print(first - second)

elif operator == "*":
    print(first*second)

elif operator == "/" :
    print(first/second)

elif operator == "%":
    print(first%second)

